//Comparison operators

console.log(1 > 2);

console.log(1 < 2);

//comparing characters

console.log('a' > 'b');

console.log('a' < 'b');

console.log('Z' > 'a');

//Equality operator

console.log(2 == '2');

//Strict comparison operator (this will always check the type of)
console.log(2 === '2');

console.log(typeof 2);

console.log(typeof '2');


//not equal 
 
console.log(2 != 4);

console.log('hello' != 'hello');

//compare Null && Undefined

console.log(null == undefined);

console.log(null === undefined);




